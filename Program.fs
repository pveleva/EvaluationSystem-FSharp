let webApp =
     choose [
        GET >=> choose allGetRoutes
        POST >=> choose allPostRoutes
        PUT >=> choose allPutRoutes
        DELETE >=> choose allDeleteRoutes
        setStatusCode 404 >=> text "Not Found" ]

