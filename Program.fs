type Message =
    {
        Text : string
    }

// ---------------------------------
// Views
// ---------------------------------

module Views =
    open Giraffe.ViewEngine

    let layout (content: XmlNode list) =
        html [] [
            head [] [
                title []  [ encodedText "API" ]
                link [ _rel  "stylesheet"
                       _type "text/css"
                       _href "/main.css" ]
            ]
            body [] content
        ]

    let partial () =
        h1 [] [ encodedText "API" ]

    let index (model : Message) =
        [
            partial()
            p [] [ encodedText model.Text ]
        ] |> layout

// ---------------------------------
// Web app
// ---------------------------------

let allGetRoutes: HttpHandler list =
    [ route "/" >=> text "Public endpoint."]
    @ answersGetRoutes

let allPostRoutes: HttpHandler list = answersPostRoutes 
let allPutRoutes: HttpHandler list = answersPutRoutes 
let allDeleteRoutes: HttpHandler list = answersDeleteRoutes

